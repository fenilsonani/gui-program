        private void button1_Click(object sender, EventArgs e)
        {
            int num=Convert.ToInt32(textBox1.Text);
            //code for prime or not
            int count = 0;
            for (int i = 1; i <= num; i++)
            {
                if (num % i == 0)
                {
                    count++;
                }
            }
            if (count == 2)
            {
                label3.Text = "You Number Is a prime";
            }
            else
            {
                label3.Text = "You Number Is not a prime";
            }
        }
